package com.project.vo;

public class CompanyVO {

	private int company_no;
	private String company_name;
	private String company_x;
	private String company_y;
	private String company_addr;
	private String company_image;
	
	
	public int getCompany_no() {
		return company_no;
	}
	public void setCompany_no(int company_no) {
		this.company_no = company_no;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getCompany_x() {
		return company_x;
	}
	public void setCompany_x(String company_x) {
		this.company_x = company_x;
	}
	public String getCompany_y() {
		return company_y;
	}
	public void setCompany_y(String company_y) {
		this.company_y = company_y;
	}
	public String getCompany_addr() {
		return company_addr;
	}
	public void setCompany_addr(String company_addr) {
		this.company_addr = company_addr;
	}
	public String getCompany_image() {
		return company_image;
	}
	public void setCompany_image(String company_image) {
		this.company_image = company_image;
	}
	
	
	
}
